package com.Otdescrption.Dto;



public class Cookie {
 private String description;
 private String thirdPartyDescription;
 private String cookieId;
 private String customCategoryName;

 // Getters and Setters
 public String getDescription() {
 return description;
 }

 public void setDescription(String description) {
this.description = description;
 }

 public String getThirdPartyDescription() {
 return thirdPartyDescription;
}

 public void setThirdPartyDescription(String thirdPartyDescription) {
this.thirdPartyDescription = thirdPartyDescription;
 }

 public String getCookieId() {
return cookieId;
 }

 public void setCookieId(String cookieId) {
 this.cookieId = cookieId;
 }

 public String getCustomCategoryName() {
 return customCategoryName;
 }

 public void setCustomCategoryName(String customCategoryName) {
 this.customCategoryName = customCategoryName;
 }
}
